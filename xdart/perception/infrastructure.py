"""
XDART-Φ × XHEART — Infrastructure Cascade Model

Ported from WorldMonitor infrastructure-cascade logic.

Models how disruptions propagate through interconnected global infrastructure.
Dependency graph links undersea cables, pipelines, ports, chokepoints, and
countries with weighted edges representing capacity dependencies.

Cascade propagation: BFS, depth ≤ 3
Impact: strength = edge_weight × disruption_level × (1 - redundancy)

Strategic chokepoints:
  - Strait of Hormuz: 80% Japan oil, 70% SK oil, 60% India oil, 40% China oil
  - Suez Canal: EU-Asia trade (Germany, Italy, UK, China)
  - Malacca Strait: 80% China oil transit
  - Bab el-Mandeb: Red Sea trade route to Suez
  - Turkish Straits: Black Sea → Mediterranean
  - Panama Canal: Americas east-west trade
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from enum import Enum

logger = logging.getLogger("xdart.perception.infrastructure")


# ─────────────────────────────────────────────────────────────────────
# Node Types
# ─────────────────────────────────────────────────────────────────────

class NodeType(str, Enum):
    CHOKEPOINT = "chokepoint"
    PORT = "port"
    PIPELINE = "pipeline"
    CABLE = "cable"
    COUNTRY = "country"


@dataclass
class InfraNode:
    """A node in the infrastructure dependency graph."""
    id: str
    name: str
    node_type: NodeType
    region: str = ""
    weight: float = 1.0          # node importance (0-1)
    redundancy: float = 0.0      # 0 = no redundancy, 1 = fully redundant


@dataclass
class InfraEdge:
    """Directed dependency edge between infrastructure nodes."""
    source_id: str
    target_id: str
    weight: float                # dependency strength (0-1)
    dependency_type: str = ""    # "oil", "trade", "data", "energy"


@dataclass
class CascadeAlert:
    """Alert generated by infrastructure cascade simulation."""
    trigger_node: str
    affected_nodes: list[str]
    max_depth: int
    total_impact: float
    summary: str
    affected_countries: list[str]


