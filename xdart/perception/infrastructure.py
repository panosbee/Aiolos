# ─────────────────────────────────────────────────────────────────────
# Infrastructure Graph
# ─────────────────────────────────────────────────────────────────────

class InfrastructureGraph:
    """Dependency graph for infrastructure cascade modeling.

    Supports disruption simulation via BFS propagation (max depth 3).
    """

    def __init__(self):
        self._nodes: dict[str, InfraNode] = {}
        self._adjacency: dict[str, list[InfraEdge]] = {}
        self._build_graph()

    def _build_graph(self):
        """Build graph from curated infrastructure data."""
        # Register all nodes
        for collection in [CHOKEPOINTS, PORTS, PIPELINES, CABLES, COUNTRY_NODES]:
            for node_id, node in collection.items():
                self._nodes[node_id] = node
                if node_id not in self._adjacency:
                    self._adjacency[node_id] = []

        # Register edges
        for edge in EDGES:
            if edge.source_id not in self._adjacency:
                self._adjacency[edge.source_id] = []
            self._adjacency[edge.source_id].append(edge)

    def get_node(self, node_id: str) -> InfraNode | None:
        return self._nodes.get(node_id)

    @property
    def node_count(self) -> int:
        return len(self._nodes)

    @property
    def edge_count(self) -> int:
        return sum(len(edges) for edges in self._adjacency.values())

    def simulate_disruption(
        self,
        node_id: str,
        disruption_level: float = 1.0,
        max_depth: int = 3,
    ) -> CascadeAlert | None:
        """Simulate disruption at a node and propagate through dependencies.

        disruption_level: 0.0 (no disruption) to 1.0 (total shutdown)
        max_depth: BFS propagation depth limit

        Returns CascadeAlert with all affected nodes and countries.
        """
        if node_id not in self._nodes:
            return None

        trigger = self._nodes[node_id]
        affected: dict[str, float] = {}  # node_id → impact strength
        visited: set[str] = {node_id}
        queue: list[tuple[str, float, int]] = [(node_id, disruption_level, 0)]

        while queue:
            current_id, current_strength, depth = queue.pop(0)
            if depth >= max_depth:
                continue

            for edge in self._adjacency.get(current_id, []):
                target_id = edge.target_id
                if target_id in visited:
                    continue

                target_node = self._nodes.get(target_id)
                if not target_node:
                    continue

                # Impact: edge_weight × disruption_level × (1 - redundancy)
                impact = edge.weight * current_strength * (1.0 - target_node.redundancy)

                if impact < 0.05:
                    continue  # negligible impact, stop propagation

                affected[target_id] = max(affected.get(target_id, 0.0), round(impact, 3))
                visited.add(target_id)
                queue.append((target_id, impact, depth + 1))

        if not affected:
            return None

        # Extract affected countries
        affected_countries = [
            nid for nid, node in self._nodes.items()
            if nid in affected and node.node_type == NodeType.COUNTRY
        ]

        total_impact = sum(affected.values())

        # Build summary
        top_affected = sorted(affected.items(), key=lambda x: x[1], reverse=True)[:5]
        summary_parts = []
        for nid, impact in top_affected:
            node = self._nodes[nid]
            summary_parts.append(f"{node.name}: {impact:.0%} impact")

        summary = f"Disruption at {trigger.name} ({disruption_level:.0%} level) cascades to {len(affected)} nodes. " \
                  f"Top impacts: {'; '.join(summary_parts)}"

        alert = CascadeAlert(
            trigger_node=node_id,
            affected_nodes=list(affected.keys()),
            max_depth=max_depth,
            total_impact=round(total_impact, 2),
            summary=summary,
            affected_countries=affected_countries,
        )

        logger.info("[InfraCascade] %s", summary)
        return alert

    def find_nearest_infrastructure(
        self,
        event_text: str,
    ) -> list[tuple[str, InfraNode]]:
        """Find infrastructure nodes mentioned in event text (keyword match).

        Returns list of (node_id, node) matches.
        """
        text_lower = event_text.lower()
        matches = []

        for node_id, node in self._nodes.items():
            # Check node name keywords
            name_words = node.name.lower().split()
            for word in name_words:
                if len(word) >= 4 and word in text_lower:
                    matches.append((node_id, node))
                    break

        return matches

    def get_chokepoint_dependencies(self, chokepoint_id: str) -> list[dict]:
        """Get all downstream dependencies of a chokepoint."""
        result = []
        for edge in self._adjacency.get(chokepoint_id, []):
            target = self._nodes.get(edge.target_id)
            if target:
                result.append({
                    "target": target.name,
                    "target_type": target.node_type.value,
                    "dependency_weight": edge.weight,
                    "dependency_type": edge.dependency_type,
                    "redundancy": target.redundancy,
                })
        return result


# Module-level singleton
_graph: InfrastructureGraph | None = None


def get_infrastructure_graph() -> InfrastructureGraph:
    """Get or create the infrastructure graph singleton."""
    global _graph
    if _graph is None:
        _graph = InfrastructureGraph()
    return _graph
