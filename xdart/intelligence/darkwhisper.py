"""
XDART-Φ × Dark Whisper Intelligence — Synthesis Engine
========================================================

DarkWhisperEngine is the analytical core of the dark intelligence layer.
It takes triaged dark signals and synthesizes them with clean perceptual
patterns to produce intelligence reports — Dark Whisper Syntheses.

THE SYNTHESIS PROCESS:

  INPUT: Triaged YES/MAYBE dark signals + Active clean patterns from ProactiveEngine

  STEP 1 — ENTITY/TOPIC OVERLAP DETECTION
    For each dark signal, compare its content against active clean patterns
    to find thematic/entity overlap. Overlap = same actors, domains, regions,
    or strategic themes present in both.

  STEP 2 — GAP DETECTION & CREATIVE NEXUS BRIDGE
    If a dark signal is relevant but the connection to clean patterns is unclear
    (the "gap"), the Creative Nexus (imagination engine) is invoked:
      • Targeted concept pairing: (dark_concept ↔ clean_concept)
      • LLM generates an imaginative hypothetical connection
      • Link is stored in MongoDB creative_links with tag="dark_whisper_gap"
      • Result is ALWAYS labeled SPECULATIVE in the output

  STEP 3 — LLM SYNTHESIS
    LLM synthesizes: clean_evidence + dark_signal + (optional imagination link)
    → produces a Dark Whisper Synthesis report with:
      • summary of what is known (clean)
      • what the dark signal adds
      • what the imagination bridge suggests (if used)
      • overall hypothesis
      • confidence score
      • verdict: YES / NO / MAYBE

  STEP 4 — ROUTING
    YES   → feed_signal() to ProactiveEngine.accumulator as source_type="dark_whisper"
    NO    → log and discard
    MAYBE → park in MongoDB dark_whispers_dormant; reactivate when new evidence arrives

  STEP 5 — DORMANT REACTIVATION
    On every synthesis cycle, dormant MAYBE signals are checked against
    new evidence. If entity/topic overlap >= DARKWEB_REACTIVATION_THRESHOLD,
    the dormant signal is reactivated and re-enters the synthesis queue.

ATTRIBUTION FORMAT (mandatory — crystal clear):
  "DARK WHISPER SYNTHESIS:
   Επαληθευμένα: [A, B, C] (καθαρές πηγές) +
   2 dark whispers (Telegram/pastebin, triage_score=0.61) +
   Creative Nexus link: [concept_X ↔ concept_Y] (ΕΙΚΑΣΙΑ) →
   Hypothesis: [Z]. Confidence: 0.38. ΕΤΥΜΗΓΟΡΙΑ: MAYBE — continuing observation."
"""

