@dataclass
class DarkWhisperSynthesis:
    """The output of one dark whisper synthesis event.

    A synthesis pairs one or more dark signals with related clean patterns
    and produces an intelligence hypothesis with explicit attribution of
    all sources, triage scores, and creative nexus contributions.
    """

    synthesis_id: str
    created_at: str

    # Inputs
    dark_signal_summaries: list[str]       # sanitized_summary from each triaged signal
    dark_signal_channels: list[str]        # channel_name for each signal
    dark_signal_scores: list[float]        # credibility_score for each signal
    clean_pattern_topics: list[str]        # topics from matched clean patterns
    clean_pattern_headlines: list[str]     # representative headlines from clean patterns

    # Creative Nexus (gap-filling imagination — may be empty)
    creative_nexus_used: bool
    creative_link_concept_a: str
    creative_link_concept_b: str
    creative_link_hypothesis: str          # imaginative connection generated by LLM

    # Synthesis output
    hypothesis: str                        # the synthesized intelligence hypothesis
    confidence: float                      # 0-1: overall confidence
    verdict: str                           # "YES" | "NO" | "MAYBE"
    reasoning: str                         # brief analytical justification

    # Full attribution text (formatted for human reading)
    attribution_text: str

    # Routing info
    fed_to_pattern_accumulator: bool = False
    parked_as_dormant: bool = False

    def to_dict(self) -> dict:
        return asdict(self)


